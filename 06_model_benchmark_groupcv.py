#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Multi-model benchmark for single-plant LAI estimation.

The script compares common machine-learning regressors using either grouped
cross-validation by Tree_ID/ID or shuffled K-fold cross-validation. It supports a
selected-feature text file generated by 04_feature_selection_topk.py. If no
feature list is supplied, the script selects the top-K features by absolute
Pearson correlation.

Models included by default:
    GBDT, RandomForest, ExtraTrees, XGBoost if installed, KNN, SVR, PLSR, Ridge.

Outputs
-------
- model_benchmark_metrics.csv
- model_benchmark_predictions.csv
- final_selected_features.txt
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Dict, List, Optional, Tuple

import numpy as np
import pandas as pd
from sklearn.cross_decomposition import PLSRegression
from sklearn.ensemble import ExtraTreesRegressor, GradientBoostingRegressor, RandomForestRegressor
from sklearn.linear_model import Ridge
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
from sklearn.model_selection import GridSearchCV, GroupKFold, KFold, cross_val_predict
from sklearn.neighbors import KNeighborsRegressor
from sklearn.pipeline import Pipeline
from sklearn.preprocessing import StandardScaler
from sklearn.svm import SVR

try:
    from xgboost import XGBRegressor
except Exception:  # xgboost is optional
    XGBRegressor = None


DEFAULT_INPUT = "outputs/features/single_plant_lai_features.csv"
DEFAULT_OUTPUT_DIR = "outputs/model_benchmark"

VI_24 = [
    "NDVI", "RVI", "DVI", "TNDVI", "RDVI", "NGRDI", "RI", "MSR", "MSAVI", "TVI",
    "WDRVI", "GRVI", "NLI", "MTVI2", "CRI", "GNDVI", "OSAVI", "SAVI", "PVI",
    "MCARI", "RGRI", "NDRE", "TCARI_OSAVI", "RECI",
]
TEX_8 = [
    "TexNIR_Contrast_Avg", "TexNIR_Dissimilarity_Avg", "TexNIR_Homogeneity_Avg",
    "TexNIR_Energy_Avg", "TexNIR_Correlation_Avg", "TexNIR_ASM_Avg",
    "TexNIR_Entropy_Avg", "TexNIR_Mean_Avg",
]


def read_table(path: str | Path) -> pd.DataFrame:
    path = Path(path)
    if path.suffix.lower() in [".xlsx", ".xls"]:
        return pd.read_excel(path)
    return pd.read_csv(path)


def metrics(y_true: np.ndarray, y_pred: np.ndarray) -> Dict[str, float]:
    rmse = float(np.sqrt(mean_squared_error(y_true, y_pred)))
    return {
        "R2": float(r2_score(y_true, y_pred)),
        "RMSE": rmse,
        "MAE": float(mean_absolute_error(y_true, y_pred)),
        "RPD": float(np.std(y_true, ddof=1) / rmse) if rmse > 0 and len(y_true) > 1 else np.nan,
    }


def load_feature_list(path: Optional[str], df: pd.DataFrame, top_k: int) -> List[str]:
    if path:
        features = [line.strip() for line in Path(path).read_text(encoding="utf-8").splitlines() if line.strip()]
        missing = [f for f in features if f not in df.columns]
        if missing:
            raise ValueError(f"Selected feature file contains missing columns: {missing}")
        return features

    candidates = [c for c in VI_24 + TEX_8 if c in df.columns]
    if not candidates:
        candidates = [c for c in df.columns if str(c).startswith("TexNIR_") or c in VI_24]
    y = pd.to_numeric(df["LAI_Observed"], errors="coerce")
    scored = []
    for f in candidates:
        x = pd.to_numeric(df[f], errors="coerce")
        mask = x.notna() & y.notna()
        if mask.sum() < 8 or x[mask].nunique() <= 1:
            continue
        r = np.corrcoef(x[mask], y[mask])[0, 1]
        if np.isfinite(r):
            scored.append((f, abs(float(r))))
    scored.sort(key=lambda t: t[1], reverse=True)
    return [f for f, _ in scored[: min(top_k, len(scored))]]


def get_models(fast: bool = False):
    if fast:
        return {
            "GBDT": (GradientBoostingRegressor(random_state=42), {
                "model__n_estimators": [200, 400],
                "model__learning_rate": [0.03, 0.05],
                "model__max_depth": [2, 3],
            }),
            "RandomForest": (RandomForestRegressor(random_state=42, n_jobs=-1), {
                "model__n_estimators": [200],
                "model__max_depth": [None, 10],
                "model__max_features": ["sqrt", 1.0],
            }),
            "Ridge": (Ridge(), {"model__alpha": [0.1, 1.0, 10.0]}),
        }

    models = {
        "GBDT": (GradientBoostingRegressor(random_state=42), {
            "model__n_estimators": [200, 400, 600],
            "model__learning_rate": [0.01, 0.03, 0.05, 0.1],
            "model__max_depth": [2, 3, 4],
            "model__subsample": [0.6, 0.8, 1.0],
        }),
        "RandomForest": (RandomForestRegressor(random_state=42, n_jobs=-1), {
            "model__n_estimators": [200, 400],
            "model__max_depth": [None, 10, 20],
            "model__max_features": ["sqrt", 1.0],
        }),
        "ExtraTrees": (ExtraTreesRegressor(random_state=42, n_jobs=-1), {
            "model__n_estimators": [200, 400],
            "model__max_depth": [None, 10, 20],
            "model__max_features": ["sqrt", 1.0],
        }),
        "KNN": (KNeighborsRegressor(), {
            "model__n_neighbors": [3, 5, 7, 9],
            "model__weights": ["uniform", "distance"],
        }),
        "SVR_RBF": (SVR(kernel="rbf"), {
            "model__C": [0.1, 1, 10, 100],
            "model__gamma": ["scale", "auto"],
        }),
        "PLSR": (PLSRegression(), {
            "model__n_components": [2, 3, 4, 5, 6, 8, 10],
        }),
        "Ridge": (Ridge(), {
            "model__alpha": [0.01, 0.1, 1.0, 10.0, 100.0],
        }),
    }
    if XGBRegressor is not None:
        models["XGBoost"] = (XGBRegressor(random_state=42, n_jobs=-1, objective="reg:squarederror"), {
            "model__n_estimators": [100, 200, 400],
            "model__learning_rate": [0.03, 0.05, 0.1],
            "model__max_depth": [3, 5, 7],
            "model__subsample": [0.7, 1.0],
        })
    return models


def make_cv(df: pd.DataFrame, n_splits: int, grouped: bool):
    if grouped and ("Tree_ID" in df.columns or "ID" in df.columns):
        group_col = "Tree_ID" if "Tree_ID" in df.columns else "ID"
        groups = df[group_col].astype(str).to_numpy()
        if len(np.unique(groups)) >= n_splits:
            return GroupKFold(n_splits=n_splits), groups, "GroupKFold", group_col
    return KFold(n_splits=n_splits, shuffle=True, random_state=42), None, "KFold", None


def run_benchmark(input_file: str, output_dir: str, feature_file: Optional[str], top_k: int,
                  n_splits: int, grouped: bool, fast: bool) -> None:
    out_dir = Path(output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    df = read_table(input_file).replace([np.inf, -np.inf], np.nan)
    if "Status" in df.columns:
        df = df[df["Status"].astype(str).str.lower().eq("success")].copy()
    if "LAI_Observed" not in df.columns:
        raise ValueError("Input table must contain LAI_Observed.")

    selected = load_feature_list(feature_file, df, top_k=top_k)
    if not selected:
        raise ValueError("No selected features available.")
    (out_dir / "final_selected_features.txt").write_text("\n".join(selected), encoding="utf-8")

    id_cols = [c for c in ["Tree_ID", "ID", "Date"] if c in df.columns]
    df_eval = df[id_cols + selected + ["LAI_Observed"]].dropna(subset=selected + ["LAI_Observed"]).copy()
    X = df_eval[selected].to_numpy(dtype=float)
    y = df_eval["LAI_Observed"].to_numpy(dtype=float)
    cv, groups, cv_name, group_col = make_cv(df_eval, n_splits=n_splits, grouped=grouped)

    rows = []
    pred_rows = []
    for name, (estimator, param_grid) in get_models(fast=fast).items():
        print(f"Training {name}...")
        pipe = Pipeline([("scaler", StandardScaler()), ("model", estimator)])
        inner_cv = min(5, max(2, n_splits - 1))
        search = GridSearchCV(pipe, param_grid=param_grid, scoring="r2", cv=inner_cv, n_jobs=-1)
        search.fit(X, y)
        best = search.best_estimator_
        if groups is None:
            pred = cross_val_predict(best, X, y, cv=cv, n_jobs=-1)
        else:
            pred = cross_val_predict(best, X, y, groups=groups, cv=cv, n_jobs=-1)

        row = {
            "Model": name,
            "Sample_Size": int(len(y)),
            "Num_Features": int(len(selected)),
            "CV": cv_name,
            "Best_Params": str({k.replace("model__", ""): v for k, v in search.best_params_.items()}),
            **metrics(y, pred),
        }
        rows.append(row)
        tmp = pd.DataFrame({
            "Model": name,
            "Tree_ID": df_eval.get("Tree_ID", df_eval.get("ID", pd.Series(np.arange(len(df_eval))))).values,
            "Date": df_eval.get("Date", pd.Series([""] * len(df_eval))).values,
            "Observed_LAI": y,
            "Predicted_LAI": pred,
            "Residual": y - pred,
        })
        pred_rows.append(tmp)
        print(f"  {name:12s} | R2={row['R2']:.3f} | RMSE={row['RMSE']:.3f} | RPD={row['RPD']:.3f}")

    pd.DataFrame(rows).sort_values("R2", ascending=False).to_csv(
        out_dir / "model_benchmark_metrics.csv", index=False, encoding="utf-8-sig"
    )
    pd.concat(pred_rows, ignore_index=True).to_csv(
        out_dir / "model_benchmark_predictions.csv", index=False, encoding="utf-8-sig"
    )


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Multi-model benchmark for LAI estimation.")
    parser.add_argument("--input", default=DEFAULT_INPUT, help="Cleaned feature table.")
    parser.add_argument("--output-dir", default=DEFAULT_OUTPUT_DIR, help="Output directory.")
    parser.add_argument("--features", default=None, help="Optional selected-feature text file, one feature per line.")
    parser.add_argument("--top-k", type=int, default=29, help="Top-K features if --features is not supplied.")
    parser.add_argument("--n-splits", type=int, default=10, help="CV folds.")
    parser.add_argument("--no-grouped", action="store_true", help="Use shuffled KFold instead of grouped CV.")
    parser.add_argument("--fast", action="store_true", help="Use a smaller model grid for quick testing.")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    run_benchmark(
        input_file=args.input,
        output_dir=args.output_dir,
        feature_file=args.features,
        top_k=args.top_k,
        n_splits=args.n_splits,
        grouped=not args.no_grouped,
        fast=args.fast,
    )


if __name__ == "__main__":
    main()
